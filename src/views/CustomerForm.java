    private void txtCusIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCusIDActionPerformed

        try {
            String customerID = txtCusID.getText();
            CustomerDTO searchCustomer = CustomerController.searchCustomer(customerID);
            if (searchCustomer != null) {
                txtCusID.setText(searchCustomer.getId());
                txtCusName.setText(searchCustomer.getName());
                txtCusAddress.setText(searchCustomer.getAddress());
                txtCusSalary.setText(Double.toString(searchCustomer.getSalary()));

                Connection connection = DBConnection.getInstance().getConnection();

                InputStream is = this.getClass().getResourceAsStream("/reports/uniqueCustomer.jasper");
                HashMap map = new HashMap();
                map.put("id", customerID);
                JasperPrint fillReport = JasperFillManager.fillReport(is, map, connection);
                JasperViewer.viewReport(fillReport, false);

            } else {
                JOptionPane.showMessageDialog(this, "No Customer");
                clearAll();
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        }
    }//GEN-LAST:event_txtCusIDActionPerformed

