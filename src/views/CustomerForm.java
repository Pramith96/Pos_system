    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            String customerID = txtCusID.getText();
            String customerName = txtCusName.getText();
            String customerAddress = txtCusAddress.getText();
            double customerSalary = Double.parseDouble(txtCusSalary.getText());

            CustomerDTO dto = new CustomerDTO(customerID, customerName, customerAddress, customerSalary);
            boolean addCustomer = CustomerController.addCustomer(dto);
            if (addCustomer) {
                JOptionPane.showMessageDialog(this, "Customer Aded..!");
                clearAll();
                loadAllCustomers();
            } else {
                JOptionPane.showMessageDialog(this, "Customer Not Aded..!");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        }


    }//GEN-LAST:event_jButton2ActionPerformed

